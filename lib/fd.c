// Return the 'struct Fd*' for file descriptor index i
#define INDEX2FD(i)	((struct Fd*) (FDTABLE + (i)*PGSIZE))
// Return the file data pointer for file descriptor index i
#define INDEX2DATA(i)	((char*) (FILEBASE + (i)*PTSIZE))


